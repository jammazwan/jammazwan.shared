package jammazwan.raw;

import jammazwan.Surname;

public class SurnamePojo implements Surname {
	String name;

	public SurnamePojo() {
		super();
	}

	public SurnamePojo(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
